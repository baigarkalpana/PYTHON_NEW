#WAP to calculate area of triangle and rectangle


print("Area of Triangle & Rectangle")

base=int(input("\nenter base of a triangle:"))
height=int(input("enter height of a triangle:"))

width=int(input("\nenter width of rectangle:"))
length=int(input("enter length of rectangle:"))

Area_of_Triangle=(base*height)/2
Area_of_Rectangle=width*length

print("\nArea of rectangle is:",Area_of_Rectangle)
print("\nArea of triangle is:",Area_of_Triangle)

